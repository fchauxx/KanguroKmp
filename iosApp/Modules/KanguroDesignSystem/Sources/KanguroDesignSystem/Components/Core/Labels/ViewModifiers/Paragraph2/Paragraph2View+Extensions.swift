import SwiftUI

// Paragraph2 - Regular
public extension View {

    func paragraph2SecondaryMediumRegular() -> some View {
        modifier(Paragraph2SecondaryMediumRegular())
    }
}

// Paragraph2 - Bold
public extension View {

    func paragraph2SecondaryDarkBold() -> some View {
        modifier(Paragraph2SecondaryDarkBold())
    }
}

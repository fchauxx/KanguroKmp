import SwiftUI

// Callout - Regular
public extension View {

    func calloutSecondaryDarkestRegular() -> some View {
        modifier(CalloutSecondaryDarkestRegular())
    }

    func calloutSecondaryDarkRegular() -> some View {
        modifier(CalloutSecondaryDarkRegular())
    }
}

// Callout - Bold
public extension View {

    func calloutSecondaryDarkestUnderlinedBold() -> some View {
        modifier(CalloutSecondaryDarkestUnderlinedBold())
    }
}

// Callout - Black
public extension View {

    func calloutWhiteExtraBold() -> some View {
        modifier(CalloutWhiteExtraBold())
    }

    func calloutSecondaryDarkestExtraBold() -> some View {
        modifier(CalloutSecondaryDarkestExtraBold())
    }
}
